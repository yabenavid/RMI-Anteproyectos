    private void bntGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntGuardarActionPerformed
        if (cbxRol.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Por favor seleccione un valor para Rol", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            if (!txtIdentificacion.getText().isEmpty()) {
                if (!"".equals(txtNombre.getText()) && txtNombre.getText().length() <= 64) {
                    if (!"".equals(txtDepartamento.getText())) {
                        if (!"".equals(txtUsuario.getText()) && txtUsuario.getText().length() >= 8) {
                            if(!"".equals(txtContrasena.getText()) && txtContrasena.getText().length() >= 8){    
                                
                                Usuario usuario = new Usuario();
                                usuario.setRol(RolEnum.valueOf((String)cbxRol.getSelectedItem()));
                                usuario.setId(Integer.parseInt(txtIdentificacion.getText()));
                                usuario.setNombre(txtNombre.getText());
                                usuario.setDepartamento(txtDepartamento.getText());
                                usuario.setUsuario(txtUsuario.getText());
                                usuario.setContrasena(txtContrasena.getText());
                                
                                try {
                                    if(objGestionUsuariosInt.registrarUsuario(usuario)){
                                        JOptionPane.showMessageDialog(null, " Usuario Registrado exitosamente", "Información", JOptionPane.INFORMATION_MESSAGE);
                                        btnLimpiar.doClick();
                                    }else{
                                        JOptionPane.showMessageDialog(null, "Usuario NO registrado", "Error", JOptionPane.ERROR_MESSAGE);
                                    }
                                } catch (RemoteException ex) {
                                    Logger.getLogger(RegistrarUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                                }
                                
                            }else{
                               JOptionPane.showMessageDialog(null, "Por favor Ingrese un valor corecto para Contraseña, mínimo 8 caracteres ", "Advertencia", JOptionPane.WARNING_MESSAGE);  
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Por favor Ingrese un valor Correcto para Usuario, mínimo 8 caracteres.  ", "Advertencia", JOptionPane.WARNING_MESSAGE);
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Por favor Ingrese un valor para Departamento ", "Advertencia", JOptionPane.WARNING_MESSAGE);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Por favor Ingrese un valor para Nombre, máximo  64 caracteres. ", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Por favor Ingrese un valor para Identificación ", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_bntGuardarActionPerformed

